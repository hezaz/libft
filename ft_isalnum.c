#include "libft.h"

int	ft_isalnum(int c)
{
    char    set;
    set = (char)c;

    if ((set >= 'a' && set <= 'z') || (set >= 'A' && set <= 'Z'))
        return (1);
    if (set >= '0' && set <= '9')
        return (1);
    return (0);
}